import Foundation
import UIKit

extension UIColor {
    class func corporateRed() -> UIColor {
        return UIColor(red: 227 / 255, green: 82 / 255, blue: 67 / 255, alpha: 1)
    }
    
    class func corporateWhite() -> UIColor {
        return UIColor(white: 241 / 255, alpha: 1)
    }
}