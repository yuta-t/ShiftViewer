import Foundation
import UIKit

extension UIColor {
    class func corporateRed() -> UIColor {
        return UIColor(red: 202 / 255, green: 73 / 255, blue: 60 / 255, alpha: 1)
    }
    
    class func corporateWhite() -> UIColor {
        return UIColor(white: 239 / 255, alpha: 1)
    }
}